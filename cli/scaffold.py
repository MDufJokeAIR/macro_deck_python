# ── Scaffold function ──────────────────────────────────────────────────

def _slugify(name: str) -> str:
    """'My Cool Plugin' → 'my_cool_plugin'"""
    return re.sub(r"[^a-z0-9]+", "_", name.lower()).strip("_")


def scaffold(
    name: str,
    package_id: str,
    author: str = "Unknown",
    description: str = "",
    style: str = "decorator",
    output_dir: Optional[Path] = None,
) -> Path:
    """
    Scaffold a new plugin directory.

    Returns the created directory path.
    """
    from typing import Optional

    if not description:
        description = f"A Macro Deck extension: {name}"

    safe_id = _slugify(package_id.split(".")[-1])

    if output_dir is None:
        output_dir = Path.home() / ".macro_deck" / "plugins" / package_id
    else:
        output_dir = Path(output_dir) / package_id

    if output_dir.exists():
        raise FileExistsError(
            f"Plugin directory already exists: {output_dir}\n"
            "Delete it first or choose a different package_id."
        )
    output_dir.mkdir(parents=True)

    ctx = dict(
        package_id=package_id,
        name=name,
        author=author,
        description=description,
        safe_id=safe_id,
    )

    # manifest.json
    (output_dir / "manifest.json").write_text(
        _MANIFEST_TEMPLATE.format(**ctx)
    )

    # main.py
    template = _MAIN_DECORATOR_TEMPLATE if style == "decorator" else _MAIN_CLASS_TEMPLATE
    (output_dir / "main.py").write_text(template.format(**ctx))

    # requirements.txt
    (output_dir / "requirements.txt").write_text(_REQUIREMENTS_TEMPLATE)

    # README.md
    (output_dir / "README.md").write_text(_README_TEMPLATE.format(**ctx))

    # config.json (empty)
    (output_dir / "config.json").write_text("{}")

    return output_dir


# ── CLI entry point ───────────────────────────────────────────────────

def main(argv=None) -> int:
    p = argparse.ArgumentParser(
        prog="macro-deck new-plugin",
        description="Scaffold a new Macro Deck Python extension",
    )
    p.add_argument("name", help='Human-readable plugin name, e.g. "My Plugin"')
    p.add_argument("--id",     required=True, dest="package_id",
                   help='Package ID, e.g. "me.myplugin"')
    p.add_argument("--author", default="Unknown", help="Your name")
    p.add_argument("--desc",   default="",        help="Short description")
    p.add_argument("--style",  default="decorator",
                   choices=["decorator", "class"],
                   help="Authoring style: decorator (default) or class")
    p.add_argument("--out",    default=None,
                   help="Output parent directory (default: ~/.macro_deck/plugins)")
    args = p.parse_args(argv)

    try:
        out = scaffold(
            name=args.name,
            package_id=args.package_id,
            author=args.author,
            description=args.desc,
            style=args.style,
            output_dir=Path(args.out) if args.out else None,
        )
        print(f"\n✅ Plugin scaffolded at:\n   {out}\n")
        print("Next steps:")
        print(f"  1. Edit  {out}/main.py  to implement your actions")
        print(f"  2. Add pip dependencies to  {out}/requirements.txt")
        print(f"  3. Start Macro Deck — it will load your plugin automatically\n")
        return 0
    except FileExistsError as exc:
        print(f"❌ {exc}", file=sys.stderr)
        return 1
    except Exception as exc:
        print(f"❌ Scaffold failed: {exc}", file=sys.stderr)
        return 1


if __name__ == "__main__":
    sys.exit(main())
