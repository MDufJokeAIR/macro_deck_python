_MAIN_DECORATOR_TEMPLATE = '''\
"""
{name} — Macro Deck Python Extension
Author: {author}

This plugin was generated by `macro-deck new-plugin`.
Edit this file to implement your actions.

Documentation:
  https://github.com/Macro-Deck-App/Macro-Deck/wiki/Plugin-API
"""
from __future__ import annotations
import json
from macro_deck_python.sdk import (
    PluginBase, action, on_load, on_delete,
    VariableType, set_variable, get_variable,
    get_config, set_config, log_info, log_error,
)


class Main(PluginBase):
    package_id  = "{package_id}"
    name        = "{name}"
    version     = "1.0.0"
    author      = "{author}"
    description = "{description}"
    can_configure = False

    def enable(self):
        """Called when the plugin is loaded. Call super().enable() to discover @action methods."""
        super().enable()
        log_info(self, f"{{self.name}} loaded with {{len(self.actions)}} action(s)")

    def disable(self):
        """Called when the plugin is unloaded. Clean up any background threads here."""
        log_info(self, f"{{self.name}} unloaded")

    # ── Actions ───────────────────────────────────────────────────────

    @action(
        name="Hello World",
        description="A simple example action that logs a message",
        can_configure=False,
    )
    def hello_world(self, client_id: str, button):
        """This method is called when the button is pressed."""
        log_info(self, f"Hello from {{self.name}}! Triggered by {{client_id}}")

    @action(
        name="Configurable Action",
        description="An action that reads its JSON configuration",
        can_configure=True,
    )
    def configurable(self, client_id: str, button):
        """Read configuration with self.get_config() inside an @action method."""
        # In @action methods, read config via the action's own configuration attribute
        # (set by Macro Deck when the user saves the action configurator)
        cfg = json.loads(self.configuration) if self.configuration else {{}}
        text = cfg.get("text", "default value")
        log_info(self, f"Configurable triggered: text={{text!r}}")

    @action(
        name="Set Variable",
        description="Example: set a variable and use it in button labels",
        can_configure=False,
    )
    def set_counter(self, client_id: str, button):
        current = get_variable("{safe_id}_counter") or 0
        new_val = int(current) + 1
        set_variable("{safe_id}_counter", new_val, VariableType.INTEGER, self)
        log_info(self, f"Counter is now {{new_val}}")

    # ── Lifecycle hooks ───────────────────────────────────────────────

    @on_load("configurable")
    def configurable_loaded(self):
        """Called when a button with the 'configurable' action is loaded."""
        log_info(self, "A configurable button was loaded")

    @on_delete("configurable")
    def configurable_deleted(self):
        """Called when a button with the 'configurable' action is deleted."""
        log_info(self, "A configurable button was deleted")
'''

